COLUNAS = ['ano', 'mes', 'IPCA', 'IGP-M', 'INCC', 'CUB/PR', 'SELIC', 'TR', 'Poupança']
INDICES_DISPONIVEIS = ['IPCA', 'IGP-M', 'INCC', 'CUB/PR', 'SELIC', 'TR', 'Poupança']

def get_dataframe() -> pd.DataFrame:
    df = pd.DataFrame(HISTORICO_RAW, columns=COLUNAS)
    df['data'] = pd.to_datetime(df[['ano','mes']].assign(day=1).rename(columns={'ano':'year','mes':'month'}))
    return df.sort_values('data').reset_index(drop=True)

def media_movel(indice: str, janela: int) -> float:
    """Retorna a média aritmética simples dos últimos N meses do índice (% ao mês)."""
    df = get_dataframe()
    if indice not in df.columns:
        return 0.0
    serie = df[indice].dropna().tail(janela)
    return float(serie.mean()) if len(serie) > 0 else 0.0

def taxa_anual_de_media(indice: str, janela: int) -> float:
    """Converte média mensal dos últimos N meses em taxa anual equivalente (%)."""
    media_pct_mes = media_movel(indice, janela)
    taxa_aa = (pow(1 + media_pct_mes / 100, 12) - 1) * 100
    return taxa_aa

def acumulado_12m(indice: str) -> float:
    """Retorna acumulado dos últimos 12 meses (%)."""
    df = get_dataframe()
    if indice not in df.columns:
        return 0.0
    serie = df[indice].dropna().tail(12)
    acum = (np.prod(1 + serie.values / 100) - 1) * 100
    return float(acum)

def acumulado_periodo(indice: str, anos: int = 10) -> float:
    """Retorna acumulado dos últimos N anos (%)."""
    df = get_dataframe()
    if indice not in df.columns:
        return 0.0
    serie = df[indice].dropna().tail(anos * 12)
    acum = (np.prod(1 + serie.values / 100) - 1) * 100
    return float(acum)

def resumo_indicadores() -> pd.DataFrame:
    """Tabela resumo com médias e acumulados de todos os índices."""
    rows = []
    for idx in INDICES_DISPONIVEIS:
        rows.append({
            'Índice': idx,
            'Média 12m (% a.m.)': round(media_movel(idx, 12), 4),
            'Taxa anual equiv. (%)': round(taxa_anual_de_media(idx, 12), 2),
            'Acum. 12m (%)': round(acumulado_12m(idx), 2),
            'Acum. 5 anos (%)': round(acumulado_periodo(idx, 5), 1),
            'Acum. 10 anos (%)': round(acumulado_periodo(idx, 10), 1),
        })
    return pd.DataFrame(rows)
